const tierRank: Record<DeviceTier, number> = { starter: 0, balanced: 1, power: 2, advanced: 3 };

export function isModelSuitable(model: ModelDefinition, tier: DeviceTier, hasF16: boolean): boolean {
  return tierRank[tier] >= tierRank[model.minTier] && (!model.requiresF16 || hasF16);
}

export function findModel(id: string): ModelDefinition | undefined {
  return MODEL_CATALOG.find((model) => model.id === id);
}
